// Выведите в консоль четные числа от 2 до 12, используйте цикл for и условие.
for(let i = 2; i <= 12; i += 2) {
    console.log(i);
}

// При помощи цикла for выведите в консоль числа от 10 до 1, в обратном порядке.
for(let i = 10; i >= 1; i--) {
    console.log(i);
}

// Перепишите 1 и 2 цикл используя цикл while.

// Первый цикл
let i = 2;
while (i <= 12) {
    console.log(i);
    i += 2;
}


// Второй цикл
let i = 10;
while (i >= 1) {
    console.log(i);
    i--;
}